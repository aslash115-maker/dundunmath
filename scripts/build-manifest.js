// Scan ../img and emit ../img-manifest.js with `window.IMAGE_MANIFEST = [...]`.
// Re-run whenever images are added/removed.
const fs = require('fs');
const path = require('path');

const IMG_DIR = path.resolve(__dirname, '..', 'img');
const OUT = path.resolve(__dirname, '..', 'img-manifest.js');

const exts = new Set(['.png', '.jpg', '.jpeg', '.gif', '.webp', '.svg']);
const files = fs.readdirSync(IMG_DIR)
  .filter((f) => exts.has(path.extname(f).toLowerCase()))
  .sort((a, b) => a.localeCompare(b, 'zh-Hans-u-co-pinyin'));

const body = `// auto-generated by scripts/build-manifest.js — do not edit by hand
window.IMAGE_MANIFEST = ${JSON.stringify(files, null, 2)};
`;
fs.writeFileSync(OUT, body, 'utf8');
console.log(`Wrote ${files.length} entries to ${OUT}`);
